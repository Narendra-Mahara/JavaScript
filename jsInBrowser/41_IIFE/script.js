// IIFE in javascript is a design pattern which is also known as self-executing anonymous function. It is a function that runs as soon as it is defined. It is a way to execute a function immediately after it is created. It is a common pattern in javascript to create a new scope for variables so that they do not conflict with the global scope. It is also used to create a closure around the entire script so that the variables are not accessible from outside the function.
