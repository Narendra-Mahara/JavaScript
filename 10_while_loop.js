// while loop is used to execute a block of code as long as the condition is true. The syntax of while loop is:
// while (condition)
// {
//     // code to be executed
// }
// program to print numbers from 1 to 10 using while loop

let i = 1;
let num = 10;
while (i <= num) {
  console.log(i);
  i++;
}
